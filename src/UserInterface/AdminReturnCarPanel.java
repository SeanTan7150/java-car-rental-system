    public void setTable() {
        reset();
        String[] columnName = {"Car", "Booking", "Status", "Customer"};
        model.setColumnIdentifiers(columnName);
        ReturnCarTable.setDefaultEditor(Object.class, null);
        ReturnCarTable.getTableHeader().setResizingAllowed(false);
        
        BookingDao bookingDao = new BookingDao();
        bookings = bookingDao.getApproved();
        
        if (bookings != null) {
            for (Booking booking : bookings) {
                String[] tableRow = {booking.getCarId(), booking.getBookingId(), booking.getStatus(), booking.getCustomerId()};
                model.addRow(tableRow);
            }
        }
    }
    
    public void reset() {
        int rowCount = model.getRowCount();
        for (int i = rowCount - 1; i >= 0; i--) {
            model.removeRow(i);
        }
        
        ReturnButton.setEnabled(false);
    }
    
