    private void ReturnButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ReturnButtonActionPerformed
        Frame currentJFrame = (Frame) SwingUtilities.getWindowAncestor(this);
        String returned = "Car has been returned. Completed.";
        Booking approving;
        BookingDao bookingDao = new BookingDao();
        approving = bookingDao.get(select);
        approving.setStatus(returned);
        
        Integer[] options = {1,2,3,4,5};
        int ratingIndex = JOptionPane.showOptionDialog(null, "Please give a rating for the experience. (Default to 3)",
                "Rating",
                JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[2]);
        
        String rating = Integer.toString(ratingIndex + 1);
        if (rating.equals("0")) {
            approving.setRating("3");
        } else {
            approving.setRating(rating);
        }
        
        if (bookingDao.update(select, approving)) {
            setTable();
            BookingListingPanel bookingListingPanel = currentJFrame.bookingListingPanel;
            bookingListingPanel.buildTable(null, false, false);
        } 
        else{
            JOptionPane.showMessageDialog(null, "Failed to return car.");
        }
    }//GEN-LAST:event_ReturnButtonActionPerformed

